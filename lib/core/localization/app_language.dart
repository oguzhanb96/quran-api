  static String of(
    BuildContext context,
    String key, [
    Map<String, String>? vars,
  ]) {
    String code;
    try {
      code =
          ProviderScope.containerOf(context).read(appLocaleProvider).languageCode;
    } catch (_) {
      code = Localizations.localeOf(context).languageCode;
    }
    var s = lookupAppExtra(key, code) ??
        values[code]?[key] ??
        values['en']?[key] ??
        values['tr']?[key] ??
        key;
    if (vars != null) {
      for (final e in vars.entries) {
        s = s.replaceAll('{${e.key}}', e.value);
      }
    }
    return s;
  }
